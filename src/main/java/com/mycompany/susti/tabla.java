public void refrescartabla(){
  while(modelo.getRowCount()>0){
      modelo.removeRow(0);
  }
 for(Alumno alumno: lista ){
     Object nuevo[]=new Object[4];
     nuevo[0]=alumno.getNombre();
     nuevo[1]=alumno.getCodigo();
     nuevo[2]=alumno.getCurso();
     nuevo[3]=alumno.getPromedio();   
     modelo.addRow(nuevo);
 }
   tb_registro.setModel(modelo);
}
