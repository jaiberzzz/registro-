    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
int nota1,nota2,nota3;
double media;
String estado = null;
    
nota1= Integer.parseInt(n1.getText());
nota2= Integer.parseInt(n2.getText());
nota3= Integer.parseInt(n3.getText());
media= (nota1+nota2+nota2)/3;
if(media>10){
    estado="aprobado";
}else if(media<=10){
    estado="desaprobado";    
    }
      
     try{
     Alumno alumno= new Alumno();
     alumno.setNombre(txtnombre.getText());
     alumno.setCodigo(txtcodigo.getText());
     alumno.setCurso(txtcurso.getText());
     alumno.setPromedio(String.valueOf(estado));
    lista.add(alumno);
    refrescartabla();
    }catch (Exception e){
        JOptionPane.showMessageDialog(this,"error");
    }
    }//GEN-LAST:event_btn_agregarActionPerformed

